<script>
import authService from "../../auth/apiService";
export default {
  data() {
    return {
      form: {
        email: "",
        password: "",
      },
      busy: false,
      stateValidate: {
        email: null,
        errorEmail: "",
        password: null,
        errorPassword: "",
      },
    };
  },
  created() {
    // Remove layout khi vào trang login
    this.$emit("update:layout", "div");
  },
  mounted() {
    // Focus input email khi render xong UI
    this.$refs.email.focus();
  },
  methods: {
    async onLogin() {
      // Bật loading
      this.busy = true;
      var res = await authService.methods.login(this.form);
      if (res.data.code === 1) {
        this.busy = false;
        this.$store.commit("setAuthenticated", {
          userData: res.data.data,
          isAuthenticated: true,
        });
        this.$router.push("/");
      } else {
        this.busy = false;
        if (res.data.code === 0) {
          // Thông báo khi không tìm thấy email
          if (res.data.msg === "email-not-found") {
            this.stateValidate.email = false;
            this.stateValidate.errorEmail = res.data.message;
            this.$refs.email.focus();
          }
          // Thông báo khi sai password
          if (res.data.msg === "wrong-password") {
            this.stateValidate.password = false;
            this.stateValidate.errorPassword = res.data.message;
            this.$refs.password.focus();
          }
        }
      }
    },
  },
};
</script>
